/**
 * var variables can be reassigned as well as redefined
 * let variables can be reassigned but cannot be redefined
 * const variables can neither be reassigned nor be redefined
 */

var name = 'Tanvi Bhatia';
console.log(name);
var name = 'Tanvi Iyer';
console.log(name);

